public class Arithmetic_Operations {
    public static void main(String args[]) {
      System.out.println(3);
      int x = 99;
      int y = 3;
      int z = x-y; // tests isub
      System.out.println(z);
      z = x*y; // tests imul
      System.out.println(z);
      z = x/y; // tests idiv
      System.out.println(z);
      z = x % y; // tests irem
      System.out.println(z);
      z = -x; // tests ineg
      System.out.println(z);
      z = x << y;
      System.out.println(z);
      z = x >> y;
      System.out.println(z);
      z = x >>> y;
      System.out.println(z);
      z = x & y;
      System.out.println(z);
      z = x | y;
      System.out.println(z);
      z = x ^ y;
      System.out.println(z);
    }
}
