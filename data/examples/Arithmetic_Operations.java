public class Arithmetic_Operations {
    public static void main(String args[]) {
      System.out.println(3);
      int x = 99;
      int y = 3;
      int z = x-y; // tests isub
      z = x*y; // tests imul
      z = x/y; // tests idiv
      z = x % y; // tests irem
      z = -x; // tests ineg
      z = x << y;
      z = x >> y;
      z = x >>> y;
      z = x & y;
      z = x | y;
      z = x ^ y;
    }
}